
# Operadores Aritimeticos

# Adicao

adicao = 10 + 10
print('Adicao: ', adicao)

# Subtracao

subtracao = 10 - 5
print('Subtracao: ', subtracao)


# Multiplicacao

multiplicacao = 2 * 3
print('Multiplicacao: ', multiplicacao)

# Divisao

divisao = 15 / 2
print('Divisao: ', divisao)

# Divisao Inteira 

divisao_inteira = 15 // 2
print('Divisao inteira: ', divisao_inteira)

# Modulo (resto da divisao)

modulo = 15 % 2
print('Modulo: ', modulo)



